public class OneDimensionalArray {
    public static void main(String[] args) {
        
        char[] letters = new char[5];
        letters[0] = 'a';
        letters[1] = 's';
        letters[2] = 'd';
        letters[3] = 'f';
        letters[4] = 'g';

        for (int i = 0; i < letters.length; i++) {
                System.out.println(letters[i]);
        }
    }
}
